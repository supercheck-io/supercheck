function fixtureIds(fixtures = SEEDED_LIVE_FIXTURES) {
  return fixtures.map((fixture) => fixture.id);
}

function incidentIdPlaceholder(index) {
  return `018f0000-0000-7000-8000-${String(index + 1).padStart(12, "0")}`;
}

function incidentMapForFixtures(selectedFixtureIds) {
  return Object.fromEntries(
    selectedFixtureIds.map((fixtureId, index) => [fixtureId, incidentIdPlaceholder(index)])
  );
}

function buildSreEvalReleaseEnvTemplate(options = {}) {
  const channel = options.channel || "release_candidate";
  const selectedFixtureIds = options.fixtureIds?.length
    ? options.fixtureIds
    : ["connector-investigation-oss-lab-checkout-degradation"];

  return [
    "# Generated by npm run sre-eval:release-env",
    "# Use only against a non-production seeded SuperCheck tenant.",
    "# Do not commit populated secret values.",
    `SRE_EVAL_RELEASE_CHANNEL=${channel}`,
    "SRE_EVAL_LIVE_ENABLED=true",
    "SRE_EVAL_MODEL_GRADE_ENABLED=true",
    "SRE_EVAL_BASE_URL=https://seeded-supercheck.example",
    "SRE_EVAL_AUTH_TOKEN=<release-eval-api-token>",
    `SRE_EVAL_FIXTURE_IDS=${selectedFixtureIds.join(",")}`,
    `SRE_EVAL_INCIDENT_IDS='${JSON.stringify(incidentMapForFixtures(selectedFixtureIds))}'`,
    "SRE_EVAL_EVALUATED_MODEL_ID=aisre-investigator-release-model",
    "SRE_EVAL_GRADER_MODEL_ID=aisre-independent-grader-model",
    "SRE_EVAL_MODEL_GRADE_MIN_SCORE=0.8",
  ].join("\n");
}

function buildSreEvalReleaseRunbookMarkdown(options = {}) {
  const selectedFixtureIds = options.fixtureIds?.length ? options.fixtureIds : fixtureIds();
  const selectedFixtures = SEEDED_LIVE_FIXTURES.filter((fixture) => selectedFixtureIds.includes(fixture.id));

  return [
    "# AISRE Release Eval Seed Runbook",
    "",
    "Use this checklist for release_candidate and stable channels only. The target must be a seeded non-production SuperCheck tenant.",
    "",
    "## Required Controls",
    "",
    "- Use fake/demo services and incidents only.",
    "- Use read-only connector credentials with row/time/byte limits.",
    "- Keep telemetry retention short and delete seed data after release validation.",
    "- Never run seeded live evals against customer production tenants.",
    "- Keep `SRE_EVAL_AUTH_TOKEN` in the CI secret store; never commit populated env files.",
    "",
    "## Required Environment",
    "",
    ...RELEASE_ENVIRONMENT_REQUIREMENTS.map((item) => `- ${item.name}: ${item.description}`),
    "",
    "## Seeded Fixtures",
    "",
    ...selectedFixtures.flatMap((fixture) => [
      `### ${fixture.id}`,
      "",
      `Connectors: ${fixture.connectors.join(", ")}`,
      "",
      ...fixture.seedData.map((seed) => `- ${seed}`),
      "",
    ]),
    "## Run",
    "",
    "```bash",
    "npm run test:sre-eval:release",
    "```",
  ].join("\n");
}

function parseArgs(argv) {
  const options = { format: "env", channel: "release_candidate", fixtureIds: [] };

  for (const arg of argv) {
    if (arg.startsWith("--format=")) options.format = arg.slice("--format=".length);
    if (arg.startsWith("--channel=")) options.channel = arg.slice("--channel=".length);
    if (arg.startsWith("--fixture-ids=")) {
      options.fixtureIds = arg
        .slice("--fixture-ids=".length)
        .split(",")
        .map((value) => value.trim())
        .filter(Boolean);
    }
  }

  return options;
}

function main() {
  const options = parseArgs(process.argv.slice(2));

  if (options.format === "json") {
    console.log(JSON.stringify({ requirements: RELEASE_ENVIRONMENT_REQUIREMENTS, fixtures: SEEDED_LIVE_FIXTURES }, null, 2));
    return;
  }

  if (options.format === "markdown") {
    console.log(buildSreEvalReleaseRunbookMarkdown(options));
    return;
  }

  console.log(buildSreEvalReleaseEnvTemplate(options));
}

if (require.main === module) {
  main();
}

