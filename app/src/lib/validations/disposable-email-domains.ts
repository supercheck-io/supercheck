/**
 * Checks if an email domain is a known disposable/throwaway email provider
 * @param email - The email address to check
 * @returns true if the email is from a disposable domain, false otherwise
 */
export function isDisposableEmail(email: string): boolean {
  if (!email || typeof email !== "string") {
    return false;
  }

  const domain = email.toLowerCase().split("@")[1];
  if (!domain) {
    return false;
  }

  return DISPOSABLE_EMAIL_DOMAINS.has(domain);
}

/**
 * Returns a user-friendly error message for disposable email rejection
 */
export function getDisposableEmailErrorMessage(): string {
  return "Please use a valid work or personal email address. Temporary or disposable email addresses are not allowed.";
}
